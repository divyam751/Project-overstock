function displayProducts() {
  furnitureData.map(function(elem) {
    var div = document.createElement("div");
    var imageRating = document.createElement("img");
    imageRating.setAttribute("src", elem.rating);
    imageRating.setAttribute("alt", elem.name);
    imageRating.style.width = "100px";
    imageRating.style.height = "20px";
    imageRating.style.marginBottom = "10px";
    var div = document.createElement("div");
    var image = document.createElement("img");
    image.setAttribute("src", elem.image_url);
    image.setAttribute("alt", elem.name);

    var priceBox = document.createElement("div");
    var finalPrice = document.createElement("h5i");
    finalPrice.textContent = "Sale Starts at INR " + elem.price;

    var productName = document.createElement("p");
    productName.textContent = elem.name;
    productName.style.marginBottom = "10px"; // add margin between the name and the button

    priceBox.append(finalPrice);

    image.addEventListener("click", function() {
      window.location.href = "productdetails.html?id=" + elem.id;
    });
    
    productName.addEventListener("click", function() {
      window.location.href = "productdetails.html?id=" + elem.id;
    });

    div.append(image, priceBox,imageRating, productName);
    document.querySelector("#furData").append(div);
  });
}

document.querySelector("#sortSelect").addEventListener("change", function() {
  var sortValue = this.value;
  var sortedData = sortProducts(furnitureData, sortValue);
  document.querySelector("#furData").innerHTML = "";
  displaySortedProducts(sortedData);
});

function sortProducts(data, sortValue) {
  if (sortValue === "lowToHigh") {
    return data.sort(function(a, b) {
      return a.price - b.price;
    });
  } else if (sortValue === "highToLow") {
    return data.sort(function(a, b) {
      return b.price - a.price;
    });
  } else {
    return data;
  }
}

